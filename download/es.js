document.getElementById("1downloadButton").addEventListener("click", function() {
    // 다운로드할 파일의 경로와 파일명 설정
    var fileURL = "SEPo-PAPPEr0.0.exe";
    var fileName = "SEPo-PAPPEr0.0.exe";

    // 새로운 a 태그를 생성하여 다운로드 링크 생성
    var a = document.createElement("a");
    a.href = fileURL;
    a.download = fileName;

    // a 태그를 클릭하여 파일 다운로드 실행
    a.click();
});

